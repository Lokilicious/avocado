    @Test
    @Transactional
    void putNewTeam() throws Exception {
        // Initialize the database
        teamRepository.saveAndFlush(team);

        int databaseSizeBeforeUpdate = teamRepository.findAll().size();

        // Update the team
        Team updatedTeam = teamRepository.findById(team.getId()).get();
        // Disconnect from session so that the updates on updatedTeam are not directly saved in db
        em.detach(updatedTeam);
        updatedTeam
            .name(UPDATED_NAME)
            .advocate(UPDATED_ADVOCATE)
            .coach(UPDATED_COACH)
            .currentlyCoached(UPDATED_CURRENTLY_COACHED)
            .numMembers(UPDATED_NUM_MEMBERS)
            .createdDate(UPDATED_CREATED_DATE);

        restTeamMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedTeam.getId())
                    .with(csrf())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedTeam))
            )
            .andExpect(status().isOk());

        // Validate the Team in the database
        List<Team> teamList = teamRepository.findAll();
        assertThat(teamList).hasSize(databaseSizeBeforeUpdate);
        Team testTeam = teamList.get(teamList.size() - 1);
        assertThat(testTeam.getName()).isEqualTo(UPDATED_NAME);
        assertThat(testTeam.getAdvocate()).isEqualTo(UPDATED_ADVOCATE);
        assertThat(testTeam.getCoach()).isEqualTo(UPDATED_COACH);
        assertThat(testTeam.getCurrentlyCoached()).isEqualTo(UPDATED_CURRENTLY_COACHED);
        assertThat(testTeam.getNumMembers()).isEqualTo(UPDATED_NUM_MEMBERS);
        assertThat(testTeam.getCreatedDate()).isEqualTo(UPDATED_CREATED_DATE);
    }

