    private static final String DEFAULT_NAME = "AAAAAAAAAA";
    private static final String UPDATED_NAME = "BBBBBBBBBB";

    private static final String DEFAULT_ADVOCATE = "AAAAAAAAAA";
    private static final String UPDATED_ADVOCATE = "BBBBBBBBBB";

    private static final String DEFAULT_COACH = "AAAAAAAAAA";
    private static final String UPDATED_COACH = "BBBBBBBBBB";

    private static final Boolean DEFAULT_CURRENTLY_COACHED = false;
    private static final Boolean UPDATED_CURRENTLY_COACHED = true;

    private static final Long DEFAULT_NUM_MEMBERS = 1L;
    private static final Long UPDATED_NUM_MEMBERS = 2L;

    private static final Instant DEFAULT_CREATED_DATE = Instant.ofEpochMilli(0L);
    private static final Instant UPDATED_CREATED_DATE = Instant.now().truncatedTo(ChronoUnit.MILLIS);

    private static final String ENTITY_API_URL = "/api/teams";
    private static final String ENTITY_API_URL_ID = ENTITY_API_URL + "/{id}";

    @Autowired
    private TeamRepository teamRepository;

    @Autowired
    private EntityManager em;

    @Autowired
    private MockMvc restTeamMockMvc;

    private Team team;

    /**
     * Create an entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Team createEntity(EntityManager em) {
        Team team = new Team()
            .name(DEFAULT_NAME)
            .advocate(DEFAULT_ADVOCATE)
            .coach(DEFAULT_COACH)
            .currentlyCoached(DEFAULT_CURRENTLY_COACHED)
            .numMembers(DEFAULT_NUM_MEMBERS)
            .createdDate(DEFAULT_CREATED_DATE);
        return team;
    }

    /**
     * Create an updated entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Team createUpdatedEntity(EntityManager em) {
        Team team = new Team()
            .name(UPDATED_NAME)
            .advocate(UPDATED_ADVOCATE)
            .coach(UPDATED_COACH)
            .currentlyCoached(UPDATED_CURRENTLY_COACHED)
            .numMembers(UPDATED_NUM_MEMBERS)
            .createdDate(UPDATED_CREATED_DATE);
        return team;
    }

    @BeforeEach
    public void initTest() {
        team = createEntity(em);
    }

    @Test
    @Transactional
    void createTeam() throws Exception {
        int databaseSizeBeforeCreate = teamRepository.findAll().size();
        // Create the Team
        restTeamMockMvc
            .perform(
                post(ENTITY_API_URL).with(csrf()).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(team))
            )
            .andExpect(status().isCreated());

        // Validate the Team in the database
        List<Team> teamList = teamRepository.findAll();
        assertThat(teamList).hasSize(databaseSizeBeforeCreate + 1);
        Team testTeam = teamList.get(teamList.size() - 1);
        assertThat(testTeam.getName()).isEqualTo(DEFAULT_NAME);
        assertThat(testTeam.getAdvocate()).isEqualTo(DEFAULT_ADVOCATE);
        assertThat(testTeam.getCoach()).isEqualTo(DEFAULT_COACH);
        assertThat(testTeam.getCurrentlyCoached()).isEqualTo(DEFAULT_CURRENTLY_COACHED);
        assertThat(testTeam.getNumMembers()).isEqualTo(DEFAULT_NUM_MEMBERS);
        assertThat(testTeam.getCreatedDate()).isEqualTo(DEFAULT_CREATED_DATE);
    }

    @Test
    @Transactional
    void createTeamWithExistingId() throws Exception {
        // Create the Team with an existing ID
        teamRepository.saveAndFlush(team);

        int databaseSizeBeforeCreate = teamRepository.findAll().size();

        // An entity with an existing ID cannot be created, so this API call must fail
        restTeamMockMvc
            .perform(
                post(ENTITY_API_URL).with(csrf()).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(team))
            )
            .andExpect(status().isBadRequest());

        // Validate the Team in the database
        List<Team> teamList = teamRepository.findAll();
        assertThat(teamList).hasSize(databaseSizeBeforeCreate);
    }

    @Test
    @Transactional
    void getAllTeams() throws Exception {
        // Initialize the database
        teamRepository.saveAndFlush(team);

        // Get all the teamList
        restTeamMockMvc
            .perform(get(ENTITY_API_URL + "?sort=id,desc"))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_VALUE))
            .andExpect(jsonPath("$.[*].id").value(hasItem(team.getId().toString())))
            .andExpect(jsonPath("$.[*].name").value(hasItem(DEFAULT_NAME)))
            .andExpect(jsonPath("$.[*].advocate").value(hasItem(DEFAULT_ADVOCATE)))
            .andExpect(jsonPath("$.[*].coach").value(hasItem(DEFAULT_COACH)))
            .andExpect(jsonPath("$.[*].currentlyCoached").value(hasItem(DEFAULT_CURRENTLY_COACHED.booleanValue())))
            .andExpect(jsonPath("$.[*].numMembers").value(hasItem(DEFAULT_NUM_MEMBERS.intValue())))
            .andExpect(jsonPath("$.[*].createdDate").value(hasItem(DEFAULT_CREATED_DATE.toString())));
    }

